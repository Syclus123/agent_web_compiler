# Publisher
# ---------------------------------------------------------------------------


class DomainSkillPublisher:
    """Render an :class:`AgentDocument` (+ optional :class:`SiteInsight`) as a
    browser-harness domain-skill.

    Two entry points:

    - :meth:`generate_from_document` — single page. Pulls APIs from
      :class:`APISynthesizer`, selectors from the action list.
    - :meth:`generate_from_site` — multi-page. Adds URL patterns, common
      actions, and login/download flags from :class:`SiteMemory`.

    Both return a :class:`DomainSkill`. Use ``.write_to_repo(bh_repo_root)``
    to drop the file into a local clone of ``browser-use/browser-harness``.
    """

    # Cap list lengths to keep generated skills readable. Real BH skills are
    # rarely longer than ~200 lines; long auto-generated dumps don't get
    # reviewed.
    MAX_APIS = 8
    MAX_NOISE_SELECTORS = 8
    MAX_ENTRY_POINTS = 6
    MAX_COMMON_ACTIONS = 6
    MAX_GOTCHAS = 6

    # ------------------------------------------------------------------
    # Entry points
    # ------------------------------------------------------------------

    def generate_from_document(
        self,
        doc: AgentDocument,
        *,
        task: str = "scraping",
        api_candidates: list[APICandidate] | None = None,
    ) -> DomainSkill:
        """Render a single-page skill.

        Args:
            doc: The compiled :class:`AgentDocument`.
            task: BH skill filename stem. Conventionally ``"scraping"`` for
                read-only data tasks, ``"upload"`` / ``"repo-actions"`` for
                specific flows.
            api_candidates: Pre-computed APIs. If omitted, synthesizes from
                the document on the fly.
        """
        if api_candidates is None:
            from agent_web_compiler.actiongraph.api_synthesizer import APISynthesizer

            api_candidates = APISynthesizer().synthesize_from_document(doc)

        domain = self._domain_from_url(doc.source_url or "")
        md = self._render(
            domain=domain,
            task=task,
            preamble=self._preamble_from_doc(doc, api_candidates),
            apis=api_candidates,
            main_selector=self._main_content_selector(doc),
            noise_selectors=[],
            entry_points=[doc.source_url] if doc.source_url else [],
            common_actions=[],
            gotchas=list(doc.quality.warnings),
        )
        return DomainSkill(
            domain=domain,
            task=task,
            markdown=md,
            api_count=len(api_candidates),
            selector_count=sum(1 for a in doc.actions if a.selector),
        )

    def generate_from_site(
        self,
        insight: SiteInsight,
        *,
        task: str = "scraping",
        api_candidates: list[APICandidate] | None = None,
        action_graph: ActionGraphModel | None = None,
        extra_gotchas: list[str] | None = None,
    ) -> DomainSkill:
        """Render a multi-page skill backed by :class:`SiteMemory`.

        Args:
            insight: A :class:`SiteInsight` produced after observing 3+ pages
                of the site.
            task: BH skill filename stem.
            api_candidates: Merged list of APIs across the site. Optional.
            action_graph: Optional multi-page :class:`ActionGraphModel`. Used
                today only for gotchas/metadata; future versions can surface
                navigation paths.
            extra_gotchas: Manually-authored gotchas to append (e.g. "R@5
                evaluation flagged these items as low-confidence").
        """
        apis = api_candidates or []

        # Login-required is a gotcha unique to BH-published skills.
        auto_gotchas: list[str] = []
        if insight.login_required:
            auto_gotchas.append(
                "Login required — most entry points redirect to an auth page "
                "when accessed without session cookies. Use BH with the user's "
                "real Chrome (`BU_NAME` session) so cookies are present."
            )
        if insight.pages_observed > 0 and insight.avg_blocks_per_page < 5:
            auto_gotchas.append(
                f"Low content density observed ({insight.avg_blocks_per_page:.1f} "
                "blocks/page avg) — the site likely renders content client-side "
                "after `readyState==complete`; add an extra `wait(2)`."
            )
        gotchas = auto_gotchas + list(extra_gotchas or [])

        md = self._render(
            domain=insight.domain,
            task=task,
            preamble=self._preamble_from_insight(insight, apis),
            apis=apis,
            main_selector=insight.main_content_selector,
            noise_selectors=list(insight.noise_selectors),
            entry_points=list(insight.entry_points),
            common_actions=list(insight.common_actions),
            gotchas=gotchas,
        )
        return DomainSkill(
            domain=insight.domain,
            task=task,
            markdown=md,
            api_count=len(apis),
            selector_count=1 + len(insight.noise_selectors),
        )

    # ------------------------------------------------------------------
    # Rendering core
    # ------------------------------------------------------------------

    def _render(
        self,
        *,
        domain: str,
        task: str,
        preamble: str,
        apis: list[APICandidate],
        main_selector: str | None,
        noise_selectors: list[str],
        entry_points: list[str],
        common_actions: list[dict],
        gotchas: list[str],
    ) -> str:
        lines: list[str] = []
        title = task.replace("-", " ").replace("_", " ").title()
        lines.append(f"# {domain} — {title}")
        lines.append("")
        lines.append(preamble)
        lines.append("")

        # --- Do this first: APIs --------------------------------------
        safe_apis = [
            a for a in apis
            if a.safety_level == "read_only" and a.confidence >= 0.5
        ][: self.MAX_APIS]
        if safe_apis:
            lines.append("## Do this first")
            lines.append("")
            lines.append(
                "**Prefer these endpoints discovered by agent-web-compiler — "
                "no browser needed.**"
            )
            lines.append("")
            lines.append("```python")
            lines.append("import json")
            for api in safe_apis:
                endpoint = _redact_endpoint(api.endpoint)
                headers = _redact_headers(api.headers_pattern or {})
                desc = api.description or api.derived_from_action_id or api.api_id
                lines.append(f"# {desc} (confidence={api.confidence:.2f})")
                if headers:
                    lines.append(f"headers = {headers!r}")
                    lines.append(f"data = json.loads(http_get({endpoint!r}, headers=headers))")
                else:
                    lines.append(f"data = json.loads(http_get({endpoint!r}))")
            lines.append("```")
            lines.append("")

        # --- Stable selectors ----------------------------------------
        sel_section: list[str] = []
        if main_selector:
            sel_section.append(f"- **main content**: `{main_selector}`")
        if noise_selectors:
            for s in noise_selectors[: self.MAX_NOISE_SELECTORS]:
                sel_section.append(f"- noise / skip: `{s}`")
        if sel_section:
            lines.append("## Stable selectors")
            lines.append("")
            lines.extend(sel_section)
            lines.append("")

        # --- URL patterns --------------------------------------------
        if entry_points:
            lines.append("## URL patterns")
            lines.append("")
            for ep in entry_points[: self.MAX_ENTRY_POINTS]:
                lines.append(f"- `{_redact_endpoint(ep)}`")
            lines.append("")

        # --- Common workflows ----------------------------------------
        if common_actions:
            lines.append("## Common workflows")
            lines.append("")
            for ca in common_actions[: self.MAX_COMMON_ACTIONS]:
                role = ca.get("role", "action")
                freq = ca.get("frequency")
                suffix = f" — frequency {freq:.0%}" if isinstance(freq, (int, float)) else ""
                lines.append(f"- **{role}**{suffix}")
            lines.append("")

        # --- Gotchas --------------------------------------------------
        if gotchas:
            lines.append("## Gotchas")
            lines.append("")
            for g in gotchas[: self.MAX_GOTCHAS]:
                lines.append(f"- {g}")
            lines.append("")

        # --- Provenance footer ---------------------------------------
        lines.append(
            f"<!-- Auto-generated by agent-web-compiler v{awc_version}. "
            "Edit freely — subsequent regenerations preserve manual changes "
            "only if you move them outside the section headers above. -->"
        )
        lines.append("")
        return "\n".join(lines)

    # ------------------------------------------------------------------
    # Preamble helpers
    # ------------------------------------------------------------------

    @staticmethod
    def _preamble_from_doc(
        doc: AgentDocument, apis: list[APICandidate]
    ) -> str:
        host = DomainSkillPublisher._domain_from_url(doc.source_url or "")
        n_blocks = doc.block_count
        n_actions = doc.action_count
        api_note = (
            f"{len(apis)} candidate API(s) detected"
            if apis
            else "no API candidates found — browser path required"
        )
        return (
            f"`https://{host}` — one page compiled "
            f"({n_blocks} blocks, {n_actions} actions). {api_note}."
        )

    @staticmethod
    def _preamble_from_insight(
        insight: SiteInsight, apis: list[APICandidate]
    ) -> str:
        auth_note = "auth required" if insight.login_required else "public"
        api_note = (
            f"{len(apis)} candidate API(s) across the site"
            if apis
            else "no API candidates found — browser path required"
        )
        return (
            f"`https://{insight.domain}` — "
            f"{insight.pages_observed} page(s) observed ({auth_note}). {api_note}."
        )

    @staticmethod
    def _main_content_selector(doc: AgentDocument) -> str | None:
        """Pick the 'main content' selector for a single-page skill.

        Heuristic: look at the action with the highest priority; if it has a
        CSS selector, return the structural ancestor hint ``main``/``article``
        if the selector points inside one, else the selector itself. Kept
        deliberately simple — SiteInsight-based rendering has better data.
        """
        if not doc.actions:
            return None
        sel = max(doc.actions, key=lambda a: a.priority).selector
        if not sel:
            return None
        # Only return the selector if it contains a structural tag — otherwise
        # "a.primary" is usually not a great "main content" hint.
        if any(tag in sel for tag in ("main", "article", "#content", ".content")):
            return sel
        return None

    @staticmethod
    def _domain_from_url(url: str) -> str:
        from urllib.parse import urlparse

        parsed = urlparse(url)
        return parsed.netloc or parsed.path.split("/", 1)[0] or "unknown-host"
