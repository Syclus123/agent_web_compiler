# Public result object
# ---------------------------------------------------------------------------


@dataclass
class DomainSkill:
    """A rendered BH-compatible domain-skill Markdown document."""

    domain: str
    task: str
    markdown: str
    # Metadata carried alongside the text for use by test suites / CI, not
    # embedded in the markdown itself.
    api_count: int = 0
    selector_count: int = 0
    generated_at: float = field(default_factory=time.time)

    def filename(self) -> str:
        """Filename inside ``agent-workspace/domain-skills/<site>/<task>.md``."""
        # BH's tree uses lowercase, hyphen-separated task names.
        return f"{self.task}.md"

    def site_dir(self, bh_repo_root: str | Path) -> Path:
        """Directory path inside the BH repo this skill belongs to."""
        return Path(bh_repo_root) / "agent-workspace" / "domain-skills" / self._slug()

    def write_to_repo(self, bh_repo_root: str | Path, *, overwrite: bool = False) -> Path:
        """Write the markdown to ``agent-workspace/domain-skills/<slug>/<task>.md``.

        Raises:
            FileExistsError: If the target file exists and ``overwrite`` is False.
        """
        target = self.site_dir(bh_repo_root) / self.filename()
        if target.exists() and not overwrite:
            raise FileExistsError(
                f"{target} already exists. Pass overwrite=True to replace."
            )
        target.parent.mkdir(parents=True, exist_ok=True)
        target.write_text(self.markdown, encoding="utf-8")
        return target

    def _slug(self) -> str:
        """Convert domain to BH's ``<site>`` slug.

        Rules (observed from BH's repo):
        - strip ``www.`` and TLD
        - replace dots with hyphens (``stack.overflow.com`` → ``stack-overflow``)
        - lowercase
        """
        d = self.domain.lower().removeprefix("www.")
        # take everything before the last dot (chop off TLD)
        if "." in d:
            d = d.rsplit(".", 1)[0]
        return d.replace(".", "-").replace("_", "-")


