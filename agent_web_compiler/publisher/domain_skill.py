"""DomainSkillPublisher — render AWC insights as browser-harness domain-skills.

**Strategic role**: This is the reverse adapter that lets AWC contribute to
browser-harness's `agent-workspace/domain-skills/` ecosystem. Every BH
domain-skill is a Markdown file documenting a site's APIs, selectors,
URL patterns, and gotchas. AWC's :class:`SiteMemory`,
:class:`APISynthesizer`, and :class:`ActionGraphBuilder` already extract that
information — this publisher just renders it in BH's conventional layout so
it can be dropped into a PR against ``browser-use/browser-harness``.

The output follows the same section structure as the best-maintained BH
skills (see ``browser-harness/agent-workspace/domain-skills/github/``):

    # <domain> — <task>
    <preamble>

    ## Do this first
    <preferred APIs, read-only, high-confidence>

    ## Stable selectors
    <main content selector + noise selectors>

    ## URL patterns
    <entry points / hub pages from SiteMemory>

    ## Common workflows
    <per-action examples>

    ## Gotchas
    <quality warnings aggregated across the observed pages>

    <!-- Auto-generated by agent-web-compiler vX.Y.Z -->

Design constraints (mirroring :doc:`/docs/integration-with-browser-harness`):

1. **No secrets**. We scrub cookies, auth tokens, and user-specific query
   params before emitting. BH's skills are public.
2. **No user data**. Only structural / semantic information makes it in —
   never the actual content of a page the user compiled.
3. **Stable output**. Given identical inputs, we produce byte-identical
   Markdown. This matters for PR reviewers comparing diffs across versions.
4. **No file I/O in :meth:`generate`**. The method returns a string; only
   :meth:`write_to_repo` touches disk.
"""

from __future__ import annotations

import re
import time
from dataclasses import dataclass, field
from pathlib import Path
from typing import TYPE_CHECKING

from agent_web_compiler import __version__ as awc_version
from agent_web_compiler.actiongraph.models import APICandidate

if TYPE_CHECKING:  # pragma: no cover
    from agent_web_compiler.actiongraph.models import ActionGraphModel
    from agent_web_compiler.core.document import AgentDocument
    from agent_web_compiler.memory.site_memory import SiteInsight


# Headers to always strip from ``APICandidate.headers_pattern`` before publishing.
# These are the typical leaks: auth tokens, session cookies, CSRF headers, and
# any ``x-*`` custom headers that often carry user ids.
_SECRET_HEADER_PATTERNS = (
    re.compile(r"^authorization$", re.IGNORECASE),
    re.compile(r"^cookie$", re.IGNORECASE),
    re.compile(r"^x-csrf", re.IGNORECASE),
    re.compile(r"^x-auth", re.IGNORECASE),
    re.compile(r"^x-api-key$", re.IGNORECASE),
    re.compile(r"^x-session", re.IGNORECASE),
    re.compile(r"^x-user", re.IGNORECASE),
)

# Query-param keys to redact.
_SECRET_QUERY_PARAMS = frozenset({
    "token", "access_token", "id_token", "api_key", "apikey", "session",
    "sessionid", "sid", "auth", "password", "secret",
})


def _redact_headers(headers: dict[str, str]) -> dict[str, str]:
    out: dict[str, str] = {}
    for k, v in (headers or {}).items():
        if any(p.match(k) for p in _SECRET_HEADER_PATTERNS):
            out[k] = "<redacted>"
        else:
            out[k] = v
    return out


def _redact_endpoint(endpoint: str) -> str:
    """Strip secret query parameters from an endpoint URL.

    We don't import urllib here because the point isn't to *correctly* parse
    every URL — it's to ensure no obvious secrets leak into the Markdown.
    For anything that doesn't round-trip cleanly through this function, the
    publisher conservatively drops the whole query string.
    """
    if "?" not in endpoint:
        return endpoint
    base, _, qs = endpoint.partition("?")
    parts = qs.split("&")
    kept: list[str] = []
    for part in parts:
        if "=" not in part:
            kept.append(part)
            continue
        key, _, _ = part.partition("=")
        if key.lower() in _SECRET_QUERY_PARAMS:
            kept.append(f"{key}=<redacted>")
        else:
            kept.append(part)
    return f"{base}?{'&'.join(kept)}" if kept else base


