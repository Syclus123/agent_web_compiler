def test_generate_is_deterministic_for_identical_inputs() -> None:
    doc = _make_doc()
    apis = [
        APICandidate(
            api_id="api1",
            endpoint="https://api.github.com/x",
            method="GET",
            confidence=0.9,
            safety_level="read_only",
        ),
    ]
    s1 = DomainSkillPublisher().generate_from_document(doc, api_candidates=apis)
    s2 = DomainSkillPublisher().generate_from_document(doc, api_candidates=apis)
    assert s1.markdown == s2.markdown
