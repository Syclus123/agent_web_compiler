def test_write_to_repo_creates_dirs_and_writes_file(tmp_path: Path) -> None:
    doc = _make_doc(url="https://stackoverflow.com/questions/42")
    skill = DomainSkillPublisher().generate_from_document(doc, task="scraping")
    target = skill.write_to_repo(tmp_path)

    assert target.exists()
    assert target.name == "scraping.md"
    assert target.parent.name == "stackoverflow"
    assert target.read_text().startswith("# stackoverflow.com — Scraping")


def test_write_to_repo_refuses_existing_without_overwrite(tmp_path: Path) -> None:
    doc = _make_doc(url="https://github.com/x/y")
    skill = DomainSkillPublisher().generate_from_document(doc)
    skill.write_to_repo(tmp_path)

    with pytest.raises(FileExistsError):
        skill.write_to_repo(tmp_path)

    # overwrite=True should succeed
    skill.write_to_repo(tmp_path, overwrite=True)


def test_slug_handles_www_and_subdomains() -> None:
    for (domain, expected) in [
        ("www.github.com", "github"),
        ("stack.overflow.com", "stack-overflow"),
        ("example.org", "example"),
        ("single", "single"),
    ]:
        sk = DomainSkill(domain=domain, task="scraping", markdown="")
        assert sk._slug() == expected  # noqa: SLF001


